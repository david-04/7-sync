//----------------------------------------------------------------------------------------------------------------------
// Re-usable fragments
//----------------------------------------------------------------------------------------------------------------------

interface JsonSourceAndDestination {
    source: string;
    destination: string;
}

//----------------------------------------------------------------------------------------------------------------------
// A file
//----------------------------------------------------------------------------------------------------------------------

interface JsonFile extends JsonSourceAndDestination {
    created: number;
    modified: number;
    size: number;
}

//----------------------------------------------------------------------------------------------------------------------
// The root directory
//----------------------------------------------------------------------------------------------------------------------

interface JsonDatabase {
    directories: JsonDirectory[];
    files: JsonFile[];
    last: string;
}

//----------------------------------------------------------------------------------------------------------------------
// A sub-directory
//----------------------------------------------------------------------------------------------------------------------

interface JsonDirectory extends JsonDatabase, JsonSourceAndDestination { }
